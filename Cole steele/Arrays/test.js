var courses = ['PHY105', 'CHM101', 'ZOO101', 'BOT101', 'SSC101'];

console.log(courses); //[ 'PHY105', 'CHM101', 'ZOO101', 'BOT101', 'SSC101' ]

//in built Methods
// pop and push

//pop
courses.pop(); // removes the last element of the array

console.log(courses); //[ 'PHY105', 'CHM101', 'ZOO101', 'BOT101' ]


//push
courses.push('LIB001');  // adds to the last element of the array 
console.log(courses); //[ 'PHY105', 'CHM101', 'ZOO101', 'BOT101', 'LIB001' ]